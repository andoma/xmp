/*
 * Simple 2-poles resonant filter
 */
void filter_setup(int srate, int cutoff, int res, int *a0, int *b0, int *b1)
{
	/* [0-255] => [100Hz-8000Hz] */
	float fc = filter_cutoff[cutoff];
	float fs = (float)srate;
	float fg, fb0, fb1;
	float d2, d, e;

	if (res > 0xff) {
		res = 0xff;
	}

	fc *= 3.14159265358979 * 2 / fs;
	d2 = dmpfac[res >> 1];
	d = (1.0 - d2) * fc;

	if (d > 2.0)
		d = 2.0;

	d = (d2 - d) / fc;
	e = 1.0 / (fc * fc);

	fg = 1.0 / (1 + d + e);
	fb0 = (d + e + e) / (1 + d + e);
	fb1 = -e / (1 + d + e);

	*a0 = (int)(fg * FILTER_PRECISION);
	*b0 = (int)(fb0 * FILTER_PRECISION);
	*b1 = (int)(fb1 * FILTER_PRECISION);
}

